package day1;

public class Operator {
    public static void main(String[] args) {
        //연산자
        //산술연산자 (+, -, *, /, %)
        System.out.println(10 + 5);
        System.out.println(10 - 5);
        System.out.println(10 * 3);
        System.out.println(10 / 3);
        System.out.println(10.0 / 3.0); //실수의 계산은 실수가 나옴
        System.out.println(10 % 3);

        //비교연산자 (>, <, >=, <=, ==, !=)
        //true, false -> 논리값(boolean)으로 결과가 나온다.
        System.out.println(10 > 5);
        System.out.println(10 < 5);
        System.out.println(10 >= 5);
        System.out.println(10 <= 5);
        System.out.println(10 == 5);
        System.out.println(10 != 5);
        }
}
